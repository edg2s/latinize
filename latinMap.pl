#!/usr/bin/perl -w

use strict;
use warnings;
use charnames ':full';
use encoding "UTF-8";
use vars qw($ch);

my @names = <DATA>;
chomp for @names;
print "// Generated from $0 " . (scalar localtime) . "\n";
print "var latinMap = {\n";
for my $name (@names) {
	my $code = qq(\$ch = "\\N{$name}");
	{
		local *STDERR = 0;
		eval $code;
	}
	if ($@) {
		# Cannot interpret some really new additions to unicode
		print "// CANNOT FIND CHAR FOR '$name'\n";
		next;
	}
	my $approx = "";
	if ($name =~ /^LATIN (?:SUBSCRIPT )?(CAPITAL|SMALL) LETTER (?:(?:BARRED|DOTLESS|INSULAR|INVERTED|LONG|OPEN|REVERSED|SIDEWAYS|SCRIPT|TURNED) )?([A-Z][A-Za-z]?)( WITH.*|DIGRAPH.*)?$/) {
		my ($case, $a) = ($1, $2);
		$approx = ($case eq "CAPITAL") ? uc($a) : lc($a);
	}
	elsif ($name =~ /^LATIN (CAPITAL|SMALL) LIGATURE ([A-Z]+)$/) {
		my ($case, $a) = ($1, $2);
		$approx = ($case eq "CAPITAL") ? uc($a) : lc($a);
	}
	elsif ($name =~ /^LATIN LETTER SMALL CAPITAL (?:(?:BARRED|DOTLESS|INSULAR|INVERTED|LONG|OPEN|REVERSED|SIDEWAYS|SCRIPT|TURNED) )?([A-Z][A-Z]?)( WITH.*)?$/) {
		$approx = $1;
	}
	else {
		print "// CANNOT FIND APPROXIMATION FOR '$name' ($ch)\n";
		next;
	}

	next if $approx eq $ch;
	print "'$ch': '$approx', // $name\n";
	next;
}

print "};\n";

